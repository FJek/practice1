//设计模式六大原则（1）：单一职责原则
//  一个类只实现一类功能，一个方法只实现一种功能

//设计模式六大原则（2）：里氏替换原则
//  所有引用基类的地方必须能透明地使用其子类的对象。子类可以扩展父类的功能，但不能改变父类原有的功能

//设计模式六大原则（3）：依赖倒置原则
//  高层模块不应该依赖低层模块，二者都应该依赖其抽象；抽象不应该依赖细节；细节应该依赖抽象。

//设计模式六大原则（4）：接口隔离原则
//  客户端不应该依赖它不需要的接口；一个类对另一个类的依赖应该建立在最小的接口上。

//设计模式六大原则（5）：迪米特法则
//  一个对象应该对其他对象保持最少的了解。 最少知识原则

//设计模式六大原则（6）：开闭原则
//  对拓展开放，对修改关闭
